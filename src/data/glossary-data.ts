export function findGlossaryTerms(text: string): { term: string; entry: GlossaryEntry; startIndex: number }[] {
  const results: { term: string; entry: GlossaryEntry; startIndex: number }[] = [];
  // Sort terms by length (longest first) to avoid partial matches
  const sortedTerms = Object.keys(glossary).sort((a, b) => b.length - a.length);

  for (const term of sortedTerms) {
    let idx = text.indexOf(term);
    while (idx !== -1) {
      // Check if this position is already covered by a longer term
      const alreadyCovered = results.some(
        (r) => idx >= r.startIndex && idx < r.startIndex + r.term.length
      );
      if (!alreadyCovered) {
        results.push({ term, entry: glossary[term], startIndex: idx });
      }
      idx = text.indexOf(term, idx + 1);
    }
  }

  return results.sort((a, b) => a.startIndex - b.startIndex);
}
